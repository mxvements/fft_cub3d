char	*ft_strchr(const char *s, int c)
{
	int	i;
	int	c_m;

	c_m = c % 256;
	i = 0;
	if (!s)
		return (0);
	while (s[i])
	{
		if (s[i] == c_m)
			return ((char *)&s[i]);
		i++;
	}
	if (s[i] == c_m)
		return ((char *)&s[i]);
	return (0);
}
