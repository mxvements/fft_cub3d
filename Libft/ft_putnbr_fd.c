#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	t;
	long	l;

	l = (long)n;
	if (l < 0)
	{
		write(fd, "-", 1);
		l = l * (-1);
	}
	if (l < 10)
	{
		t = l + '0';
		write(fd, &t, 1);
	}
	if (l >= 10)
	{
		ft_putnbr_fd(l / 10, fd);
		t = (l % 10) + '0';
		write (fd, &t, 1);
	}
}
